import store from "@/store/index.js";
import LogoSvg from "@/components/UI-helpers/LogoSvg.vue";
import Arrow from "@/components/UI-helpers/Arrow.vue";
import CustomSelect from "@/components/CustomSelect.vue";

export default {
  components: {
    LogoSvg,
    Arrow,
    CustomSelect,
  },
  setup() {
    const tableStats = computed(() => {
      return store.getters["info/tableStats"];
    });

    const cities = ref(["dhaka", "bangkok", "new york", "islamabad"]);
    const countries = ref(["bangladesh", "india", "england", "sri lanka"]);
    const languages = ref([
      "english",
      "spanish",
      "arabic",
      "portuguise",
      "bengali",
    ]);

    const totalVisits = ref(null);
    const barWidth = ref([]);
    const getWidth = ref([]);

    tableStats.value.forEach((sum) => {
      totalVisits.value += sum.visit;
    });

    tableStats.value.forEach((width) => {
      barWidth.value = (width.visit * 100) / totalVisits.value;
      getWidth.value.push(ref(barWidth.value));
      return { getWidth };
    });

    onMounted(() => {
      const bars = document.querySelectorAll(".progress-bar");

      bars.forEach((bar, idx) => {
        let width = getWidth.value[idx];
        for (let i = 0; i < width.value; i++) {
          bar.style.width = i + 10 + "%";
        }
      });
    });

    return {
      tableStats,
      totalVisits,
      cities,
      countries,
      languages,
      barWidth,
      getWidth,
    };
  },
};
</script>

